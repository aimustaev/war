#include "libft.h"

int	ft_atoi(const char *str)
{
	char	sign;
	int		result;

	sign = 1;
	result = 0;
	while (*str == '\t' || *str == '\n' || *str == '\v'
		|| *str == '\f' || *str == '\r' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while ((*str >= '0' && *str <= '9') && *str != '\0')
	{
		result *= 10;
		result += (*str - '0') * sign;
		str++;
	}
	return (result);
}
